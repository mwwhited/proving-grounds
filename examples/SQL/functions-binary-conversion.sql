ALTER FUNCTION [dbo].[BitCount]
(
	 @value BIGINT
)
RETURNS NVARCHAR(40)
WITH SCHEMABINDING
AS
BEGIN
    RETURN (	
		SELECT COUNT([value])
		FROM (VALUES 
			 (CASE @value & 0x80000000 WHEN 0 THEN NULL ELSE '1' END)
			,(CASE @value & 0x40000000 WHEN 0 THEN NULL ELSE '1' END)
			,(CASE @value & 0x20000000 WHEN 0 THEN NULL ELSE '1' END)
			,(CASE @value & 0x10000000 WHEN 0 THEN NULL ELSE '1' END)
			,(CASE @value & 0x08000000 WHEN 0 THEN NULL ELSE '1' END)
			,(CASE @value & 0x04000000 WHEN 0 THEN NULL ELSE '1' END)
			,(CASE @value & 0x02000000 WHEN 0 THEN NULL ELSE '1' END)
			,(CASE @value & 0x01000000 WHEN 0 THEN NULL ELSE '1' END)
			,(CASE @value & 0x00800000 WHEN 0 THEN NULL ELSE '1' END)
			,(CASE @value & 0x00400000 WHEN 0 THEN NULL ELSE '1' END)
			,(CASE @value & 0x00200000 WHEN 0 THEN NULL ELSE '1' END)
			,(CASE @value & 0x00100000 WHEN 0 THEN NULL ELSE '1' END)
			,(CASE @value & 0x00080000 WHEN 0 THEN NULL ELSE '1' END)
			,(CASE @value & 0x00040000 WHEN 0 THEN NULL ELSE '1' END)
			,(CASE @value & 0x00020000 WHEN 0 THEN NULL ELSE '1' END)
			,(CASE @value & 0x00010000 WHEN 0 THEN NULL ELSE '1' END)
			,(CASE @value & 0x00008000 WHEN 0 THEN NULL ELSE '1' END)
			,(CASE @value & 0x00004000 WHEN 0 THEN NULL ELSE '1' END)
			,(CASE @value & 0x00002000 WHEN 0 THEN NULL ELSE '1' END)
			,(CASE @value & 0x00001000 WHEN 0 THEN NULL ELSE '1' END)
			,(CASE @value & 0x00000800 WHEN 0 THEN NULL ELSE '1' END)
			,(CASE @value & 0x00000400 WHEN 0 THEN NULL ELSE '1' END)
			,(CASE @value & 0x00000200 WHEN 0 THEN NULL ELSE '1' END)
			,(CASE @value & 0x00000100 WHEN 0 THEN NULL ELSE '1' END)
			,(CASE @value & 0x00000080 WHEN 0 THEN NULL ELSE '1' END)
			,(CASE @value & 0x00000040 WHEN 0 THEN NULL ELSE '1' END)
			,(CASE @value & 0x00000020 WHEN 0 THEN NULL ELSE '1' END)
			,(CASE @value & 0x00000010 WHEN 0 THEN NULL ELSE '1' END)
			,(CASE @value & 0x00000008 WHEN 0 THEN NULL ELSE '1' END)
			,(CASE @value & 0x00000004 WHEN 0 THEN NULL ELSE '1' END)
			,(CASE @value & 0x00000002 WHEN 0 THEN NULL ELSE '1' END)
			,(CASE @value & 0x00000001 WHEN 0 THEN NULL ELSE '1' END)
			) [values]([value])
    );
END;
